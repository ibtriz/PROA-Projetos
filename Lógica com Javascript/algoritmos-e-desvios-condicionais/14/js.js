// Descrição : Ler um valor e escrever a mensagem
// É MAIOR QUE 10! se o valor lido for maior que 10,
// caso contrário escrever NÃO É MAIOR QUE 10!
var valor = parseInt(prompt("Digite um valor"));
if (valor > 10) {
    alert("o valor é MAIOR que 10");
} else
if (valor < 10) {
    alert("o valor é MENOR que 10");
} else
if (valor = 10) {
    alert("o valor é IGUAL a 10");
}